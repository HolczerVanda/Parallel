#ifndef SQUAREGRIDTOPOLOGY_H
#define SQUAREGRIDTOPOLOGY_H

/**
 * Printing the number of nodes and edges in a planar grid.
 */
void countNodesAndEdges(int n);

/**
 * Printing the distance distribution in a planar grid.
 * Illustrating it like a histogram.
 */
void distanceDistributionPlanar(int size);

/**
 * Printing the number of nodes and edges in a spatial grid.
 */
void spatialGrid(int n);

/**
 * Comparing the number of edges in a planar and in a spatial grid.
 */
void numberOfEdges(int n);

/**
 * Printing the distance distribution in a spatial grid.
 */
void distanceDistributionSpatial(int size);

#endif /* SQUAREGRIDTOPOLOGY_H */